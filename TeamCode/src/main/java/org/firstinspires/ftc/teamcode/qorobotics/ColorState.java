/**
 * Color State represents which way to go
 * really a misnomer because this enum arose from the beacons in FTC
 * depending on the beacon color on each hemisphere,
 * the robot should go in a certain direction or none at all
 *
 * Created by Derek Zhang on 12/1/16.
 */

public enum ColorState {

    NA, LEFT, RIGHT;
}
